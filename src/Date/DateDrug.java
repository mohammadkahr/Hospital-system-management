package Date;

public class DateDrug {
    private int year;
    private int mount;

    public DateDrug(int year, int mount) {
        this.year = year;
        this.mount = mount;
    }

    //getters and setters
    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getMount() {
        return mount;
    }

    public void setMount(int mount) {
        this.mount = mount;
    }

}
